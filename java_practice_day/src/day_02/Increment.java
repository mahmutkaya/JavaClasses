package day_02;

public class Increment {

	public static void main(String[] args) {
		int i = 0;
		// Post=after increment means we use ++ sign to teh right side of the variable
		System.out.println(i++);// 0
		System.out.println(i);// 1

		// Pre=before increament means we use ++ sing to the left side of the variable
		int j = 0;
		System.out.println(++j);// 1
		System.out.println(j);// 1

	}

}
