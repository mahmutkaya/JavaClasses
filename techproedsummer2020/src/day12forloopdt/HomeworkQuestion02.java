package day12forloopdt;

public class HomeworkQuestion02 {

	public static void main(String[] args) {
		/*
		 * Write a program to print even counting numbers from 100 to 43 on the console
		 * by using for-loop.
		 */
		for (int i = 100; i >= 43; i--) {
			if (i % 2 == 0)
				System.out.print(i + " ");
		}

	}

}
