package day01variables;

public class Variable02 {
	
	public static void main(String[] args) {
		
	float salary = 12f; // 12 = 12.0 
	System.out.println(salary);//12.0
	
	
	float money = 12.45F;// F or f doesnt matter
	System.out.println(money);//12.45
	
	double length = 3.546;  // instead of putting  f at the end,
	                       // you can change data type to double
	System.out.println(length);  
	
	double size = 1.23f;
	System.out.println(size);
	
	
	
	}
}
